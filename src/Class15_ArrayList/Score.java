
public class Score {
	private int score;
	private int handicap;
	
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public int getHandicap() {
		return handicap;
	}
	public void setHandicap(int handicap) {
		this.handicap = handicap;
	}
	
	public Score(int score, int handicap) {
		super();
		this.score = score;
		this.handicap = handicap;
	}
	

}
